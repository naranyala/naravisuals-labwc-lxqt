#!/bin/bash
# LXQt Rice - System Fetch Tool Installer
# =========================================
# Installs and configures system info fetch tools (neofetch/fastfetch/hyfetch)
# for display in terminal on session start.
#
# Usage:
#   bash neofetch.sh                    # Interactive
#   bash neofetch.sh fastfetch          # Install specific tool
#   bash neofetch.sh --list             # List available tools
#   bash neofetch.sh --all              # Install everything
#   bash neofetch.sh --config only      # Only write config files (no install)

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
source "$SCRIPT_DIR/scripts/utils/lib.sh"

# Tools to install via package manager
PACKAGE_TOOLS=(
  "neofetch|neofetch|Classic system info tool (Python)"
  "fastfetch|fastfetch|Modern system info tool (C, much faster)"
  "hyfetch|hyfetch|Neofetch with LGBTQ+ pride flags"
  "pfetch|pfetch|Minimal system info tool (shell)"
  "screenfetch|screenfetch|Multi-platform fetch tool"
)

# Configurations to write
write_neofetch_config() {
  local config_dir="$HOME/.config/neofetch"
  local config_file="$config_dir/config.conf"

  log_step "Writing neofetch configuration"

  if [ "$DRY_RUN" = "true" ]; then
    log_dim "  [DRY-RUN] Would write: $config_file"
    return 0
  fi

  mkdir -p "$config_dir"

  cat > "$config_file" << 'NEOFIX'
# neofetch config — generated by LXQt Rice scripts
# See: https://github.com/dylanaraps/neofetch/wiki

print_info() {
    info title
    info underline
    info "OS" distro
    info "Host" model
    info "Kernel" kernel
    info "Uptime" uptime
    info "Packages" packages
    info "Shell" shell
    info "Resolution" resolution
    info "DE" de
    info "WM" wm
    info "WM Theme" wm_theme
    info "Theme" theme
    info "Icons" icons
    info "Terminal" term
    info "Terminal Font" term_font
    info "CPU" cpu
    info "GPU" gpu
    info "Memory" memory
    info cols
}

