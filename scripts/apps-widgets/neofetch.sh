# Add neofetch to labwc autostart if not already present
add_to_autostart() {
  local tool="$1"
  local autostart="$HOME/.config/labwc/autostart"

  if [ ! -f "$autostart" ]; then
    log_warn "labwc autostart not found: $autostart"
    log_dim "  Skipping autostart entry (install dotfiles first)"
    return 0
  fi

  local entry="# System fetch on terminal open — managed by lxqt-rice"
  if grep -q "neofetch\|fastfetch\|pfetch\|screenfetch" "$autostart"; then
    log_dim "  fetch tool already in autostart"
    return 0
  fi

  if [ "$DRY_RUN" = "true" ]; then
    log_dim "  [DRY-RUN] Would add to autostart: $tool"
    return 0
  fi

  # We don't auto-add — users can run fetch manually in their shell rc
  log_dim "  tip: add '$tool' to ~/.bashrc or ~/.zshrc to show on terminal start"
}

list_tools() {
  print_header "Available System Fetch Tools"
  printf "\n"
  printf "%-20s %s\n" "TOOL" "DESCRIPTION"
  printf "%s\n" "──────────────────────────────────────────"
  for entry in "${PACKAGE_TOOLS[@]}"; do
    IFS='|' read -r name _ desc <<< "$entry"
    printf "  ${GREEN}%-18s${RST} %s\n" "$name" "$desc"
  done
  printf "\n"
}

install_package() {
  local name="$1" desc="$2"

  log_step "Installing: $name"

  if cmd_exists "$name"; then
    log_ok "already installed: $name ($(command -v "$name"))"
    return 0
  fi

  if [ "$DRY_RUN" = "true" ]; then
    log_dim "  [DRY-RUN] Would install: $name"
    return 0
  fi

  log_dim "  installing via package manager..."
  if pkg_install "$name"; then
    log_ok "installed: $name"
    return 0
  fi

  # Try pip for neofetch/hyfetch
  if [ "$name" = "neofetch" ] || [ "$name" = "hyfetch" ]; then
    if cmd_exists "pip3"; then
      pip3 install --user "$name" 2>/dev/null && log_ok "installed via pip: $name" && return 0
    fi
  fi

  # Manual install for fastfetch if package manager fails
  if [ "$name" = "fastfetch" ]; then
    log_dim "  trying manual download..."
    local url
    local arch
    arch="$(uname -m)"
    case "$arch" in
      x86_64) url="https://github.com/fastfetch-cli/fastfetch/releases/latest/download/fastfetch-linux-amd64.tar.gz" ;;
      aarch64) url="https://github.com/fastfetch-cli/fastfetch/releases/latest/download/fastfetch-linux-aarch64.tar.gz" ;;
      *) log_warn "unsupported arch: $arch"; return 1 ;;
    esac

    local tmpdir="$CACHE_DIR/fastfetch"
    rm -rf "$tmpdir"
    mkdir -p "$tmpdir"
    download_extract "$url" "$tmpdir" 1 "fastfetch binary"
    find "$tmpdir" -name "fastfetch" -type f -exec cp {} "$HOME/.local/bin/" \; 2>/dev/null || true
    find "$tmpdir" -name "flashfetch" -type f -exec cp {} "$HOME/.local/bin/" \; 2>/dev/null || true
    rm -rf "$tmpdir"
    if [ -f "$HOME/.local/bin/fastfetch" ]; then
      chmod +x "$HOME/.local/bin/fastfetch"
      log_ok "manually installed: fastfetch -> ~/.local/bin/fastfetch"
      return 0
    fi
  fi

  log_warn "Could not install: $name"
  return 1
}

main() {
  print_header "LXQt Rice — System Fetch Tool Installer"

  require_cmds curl tar || true  # not fatal, some may already be installed

  local do_install=true
  local do_config=true
  local targets=()

  if [ $# -eq 0 ]; then
    list_tools
    printf "${BOLD}Enter tool names to install (space-separated) or 'all':${RST} "
    read -r input
    if [ "$input" = "all" ]; then
      for entry in "${PACKAGE_TOOLS[@]}"; do
        IFS='|' read -r name _ _ <<< "$entry"
        targets+=("$name")
      done
    elif [ "$input" = "none" ]; then
      do_install=false
      do_config=false
    else
      targets=($input)
    fi
  else
    for arg in "$@"; do
      case "$arg" in
        --list|-l) list_tools; return 0 ;;
        --all|-a)
          for entry in "${PACKAGE_TOOLS[@]}"; do
            IFS='|' read -r name _ _ <<< "$entry"
            targets+=("$name")
          done ;;
        --config) do_install=false; do_config=true ;;
        --no-config) do_config=false ;;
        --dry-run) DRY_RUN=true ;;
        --force|-f) FORCE=true ;;
        *) targets+=("$arg") ;;
      esac
    done
  fi

  local ok=true

  if [ "$do_install" = true ] && [ ${#targets[@]} -gt 0 ]; then
    for target in "${targets[@]}"; do
      local found=false
      for entry in "${PACKAGE_TOOLS[@]}"; do
        IFS='|' read -r name _ desc <<< "$entry"
        if [ "$name" = "$target" ]; then
          install_package "$name" "$desc" || ok=false
          found=true
          break
        fi
      done
      if [ "$found" = false ]; then
        log_error "Unknown tool: $target"
        ok=false
      fi
    done
  fi

  if [ "$do_config" = true ]; then
    if cmd_exists "neofetch"; then
      write_neofetch_config
    fi
    if cmd_exists "fastfetch" || [ -f "$HOME/.local/bin/fastfetch" ]; then
      write_fastfetch_config
    fi
    if cmd_exists "hyfetch"; then
      write_hyfetch_config
    fi
  fi

  if [ "$ok" = true ]; then
    print_summary "System fetch installer" "ok"
  else
    print_summary "System fetch installer" "fail"
    return 1
  fi
}

if [ "${BASH_SOURCE[0]}" = "$0" ]; then
  main "$@"
fi
